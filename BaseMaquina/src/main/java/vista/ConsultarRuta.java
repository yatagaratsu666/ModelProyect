    private void modificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_modificarMouseClicked
        String id = idFrame.getText();
        boolean status = false;
        boolean status1 = false;
        String inicio = inicioFrame.getText();
        String destino = finalFrame.getText();
        String costo = costoFrame.getText();
        String idtren = trenFrame.getText();
        String horario = horarioFrame.getText();
        Estacion estacion1 = new Estacion(inicio);
        Estacion estacion2 = new Estacion(destino);
        Tren tren = tr.buscarPasajeroPorIdentificacion(idtren);
        Ruta ruta = new Ruta(Integer.parseInt(id), estacion1, estacion2, horario, Float.parseFloat(costo), tren);
        if (rt.modificarRecorrido(Integer.parseInt(id), ruta)) {
            for (int i = 0; i < estaciones.size(); i++) {
                if (estaciones.get(i).getNombre().equals(inicio)) {
                    status = true;
                }
            }
            for (int i = 0; i < estaciones.size(); i++) {
                if (estaciones.get(i).getNombre().equals(destino)) {
                    status1 = true;
                }
            }
            if (status & status1) {
                cl.agregarEnLista("Se ha modificado la ruta de id "+id);
                ConsultarRuta cr = new ConsultarRuta();
                cr.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Estaciones incorrectas");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ruta no disponible");
        }
    }//GEN-LAST:event_modificarMouseClicked

