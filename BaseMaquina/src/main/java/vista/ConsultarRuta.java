    private void cargarRutasEnTabla(LinkedList<Ruta> rutas) {
        for (int i = 0; i < rutas.size(); i++) {
            Object[] fila = {
                rutas.get(i).getId(),
                rutas.get(i).getEstacion1().getNombre(),
                rutas.get(i).getEstacion2().getNombre(),
                rutas.get(i).getHorarios(),
                rutas.get(i).getPrecio(),
                rutas.get(i).getTren().getIdentificador(),
                rutas.get(i).getTren().getNombre()
            };
            mt.addRow(fila);
        }
    }

    private void cargarEstacionesEnTabla(LinkedList<Estacion> rutas) {
        for (int i = 0; i < rutas.size(); i++) {
            Object[] fila = {
                rutas.get(i).getNombre(),
            };
            mt1.addRow(fila);
        }
    }

    private void cargarRutaEnTabla(Ruta rutas) {
        Object[] fila = {
            rutas.getId(),
            rutas.getEstacion1().getNombre(),
            rutas.getEstacion2().getNombre(),
            rutas.getHorarios(),
            rutas.getPrecio(),
            rutas.getTren().getIdentificador(),
            rutas.getTren().getNombre()
        };
        mt.addRow(fila);
    }

