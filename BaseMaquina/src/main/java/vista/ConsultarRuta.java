        String destino = finalFrame.getText();
        String costo = costoFrame.getText();
        String idtren = trenFrame.getText();
        String horario = horarioFrame.getText();
        Estacion estacion1 = new Estacion(inicio);
        Estacion estacion2 = new Estacion(destino);
        Tren tren = tr.buscarPasajeroPorIdentificacion(idtren);
